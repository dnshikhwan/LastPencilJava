package lastpencil;

class Friend {
    private String name;
    private int pencil;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPencil() {
        return pencil;
    }

    public void setPencil(int pencil) {
        this.pencil = pencil;
    }
}